public class toLowerToUpperCase {
    public static void main(String[] args) {
        String a="GJGJGJGJ";
        String b="LRFKLTKKITK";
        System.out.println("YKTYKYK");
        System.out.println("GLYLTLT"+a+b);
        System.out.println("YKTYKYK".toLowerCase());
        System.out.println(("GLYLTLT"+a+b).toLowerCase());
        String c="ghgfjhgj";
        String d="iuuykuykli;ui";
        System.out.println(c.toUpperCase());
        System.out.println(d.toUpperCase());
        System.out.println((c+" "+d).toUpperCase());
    }

}
